    public void AdicionarLinhaPessoa() {
        FastFeet.ListaPessoa = PessoaDAO.BuscarPessoas();

        DefaultTableModel table = (DefaultTableModel) this.tabelaPessoa.getModel();

        for (int i = 0; i < FastFeet.ListaPessoa.size(); i++) {

            Object[] dado = {
                FastFeet.ListaPessoa.get(i).getNome(),
                FastFeet.ListaPessoa.get(i).getTelefone(),
                FastFeet.ListaPessoa.get(i).getPessoaId(),
                FastFeet.ListaPessoa.get(i).getEnderecoId()};
            table.addRow(dado);
        }
    }

