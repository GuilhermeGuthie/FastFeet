    private void DeleteTabela1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteTabela1ActionPerformed
        int index = this.tabelaPessoa.getSelectedRow();
        Pessoa pessoa = FastFeet.ListaPessoa.get(index);
        PessoaDAO.DeletarPessoaPorCodigo(pessoa.getPessoaId());
        this.tabelaPessoa.clearSelection();
        DefaultTableModel table = (DefaultTableModel) this.tabelaPessoa.getModel();
        table.removeRow(index);        // TODO add your handling code here:
    }//GEN-LAST:event_DeleteTabela1ActionPerformed

